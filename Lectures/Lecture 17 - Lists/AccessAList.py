# To access a list element you have to specify WHICH index you want to use
# This is done with the subscript operator [] in the same 
# way that you access individual characters in a string.

# Example 1: 

value = [9,8,7,6,5,4,3,2,1,0]
print(value[3])  # prints at the index 3

# Example 2:

prerequisites = ["CGS 2060", "Introduction to Computers and Computer Programming", 3]

print("prerequisites[0] = ",prerequisites[0])
print("prerequisites[1] = ",prerequisites[1])
print("prerequisites[2] = ",prerequisites[2])
