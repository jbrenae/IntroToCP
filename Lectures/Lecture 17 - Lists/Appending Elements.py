# Start with an empty list

goodFood = []
print(goodFood)
# A new element can be appended to the 
# end of the list with the append method:

goodFood.append("burgers")

# The size, or length, of the list increases after each call to the append method. 
# Any number of elements can be added to the END of a list:

goodFood.append("Ice Cream")
print(goodFood)
goodFood.append("hotdog")
print(goodFood)
goodFood.append("cake")
print(goodFood)