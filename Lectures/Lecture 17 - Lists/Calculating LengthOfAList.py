# Python has a built in function len()
#
# The len() method takes an argument where you provide 
# a list and it returns the length of the given list.
# 
# The method used: len(object)

# Example:

myList = [5, 10, 15, 20, 'a', 'b', 'c']

list_length = len(myList)
print(list_length)