# i is the INDEX in range (index = what position it is on the line)

#
# 
# There are four different forms of the For function.

for i in range (5):
    print(i) # Prints 0, 1, 2, 3, 4
print()

for i in range(1,5):
    print(i) # Prints 1, 2, 3, 4
print()

for i in range (1,11,2):
    print(i) # Prints 1, 3, 5, 7, 9

print()
for i in range (-10,-5):
    print(i)
